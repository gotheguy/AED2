package Dominio;


public class Ciudad 
{
    private String nombre;


    public Ciudad(String nombre) {
        this.nombre = nombre;
    }

    public Ciudad() {
        this.nombre = null;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }


    
    
}
